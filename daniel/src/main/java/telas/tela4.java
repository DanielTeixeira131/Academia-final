    public void mostra(int i){
        cadastroA con=new cadastroA();
        listadealuno = con.listaDeAlunos();
        nome.setText(listadealuno.get(i).getNome());
        telefone.setText(listadealuno.get(i).getTelefone());
        genero.setText(listadealuno.get(i).getGenero());
        peso.setText(String.valueOf(listadealuno.get(i).getPeso()));
        altura.setText(String.valueOf(listadealuno.get(i).getAltura()));
        idade.setText(String.valueOf(listadealuno.get(i).getIdade()));
    }

